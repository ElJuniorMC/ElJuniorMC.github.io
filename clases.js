function mostrardk() {
    var fragmento = document.getElementById("dk");
    fragmento.style.display = "block";
    var elementos = document.getElementsByTagName("div");
    for (var i = 0; i < elementos.length; i++) {
    var elemento = elementos[i];
    if (elemento.id !== "dk") {
      elemento.style.display = "none";
    }
  }
}
